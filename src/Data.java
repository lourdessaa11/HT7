
public class Data {
	
	String spanish;
	String english;
	String french;
	
	Data(String sp, String en, String fr){
		spanish = sp;
		english = en;
		french = fr;
	}
	
	public String getSpanish() {
		return spanish;
	}
	
	public String getEnglish() {
		return english;
	}
	
	public String getFrench() {
		return french;
	}
}
